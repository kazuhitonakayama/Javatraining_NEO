package jp.learningdesign.javastudy;

public class calc {

	public static void main(String[] args) {
		// 算術演算子 +,-,*,/,%
		int a = 15;
		int b = 4;
		System.out.println(a+b);
		System.out.println(a+b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		/*
		 * クラスメイの先頭は大文字
		 * メソッド名、変数名の先頭は小文字に
		 * メソッド名は動詞＋名刺　getValue
		 * 
		 * 定数名は全て大文字
		 * 
		 */
	}

}
